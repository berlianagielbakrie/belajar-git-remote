<?php

/**Operator aritmatika
 * +$variable               positif
 * -$variable               negatif
 * $variable + $variable    penambahan
 * $variable - $variable    pengurangan
 * $variable * $variable    perkalian
 * $variable / $variable    pembagian
 * $variable % $variable    sisa bagi
 * $variable ** $variable   pangkat
 */

 $tambah = 10 + 10;
 var_dump($tambah);

 $sisabagi = 100 % 3;
 var_dump($sisabagi);

 $tambahnegatif = -$tambah;
 var_dump($tambahnegatif);

 $tambahpositif = +$tambah;
 var_dump($tambahpositif);